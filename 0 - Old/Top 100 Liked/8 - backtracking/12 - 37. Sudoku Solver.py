from typing import List


class Solution:
    def solveSudoku(self, board: List[List[str]]) -> None:
        """
        Do not return anything, modify board in-place instead.
        """

        rows, cols = len(board), len(board[0])

        def is_valid(row, col, num):

            for i in range(9):
                if board[row][i] == num:
                    return False
                if board[i][col] == num:
                    return False
                if board[3 * (row // 3) + i // 3][3 * (col // 3) + i % 3] == num:
                    return False
            return True

        def backtrack():

            for i in range(rows):
                for j in range(cols):
                    if board[i][j] == ".":
                        for num in range(1, 10):
                            if is_valid(i, j, str(num)):
                                board[i][j] = str(num)
                                if backtrack():
                                    return True
                                board[i][j] = "."
                        return False
            return True

        backtrack()


